public class tugas8perulangan {
    public static void main(String[] args) {
        // Inisialisasi variabel penghitung
        int i = 1;
        
        // Perulangan while untuk menampilkan kalimat 10 kali
        while (i <= 10) {
            System.out.println("Saya berjanji tidak akan mengulangi kesalahan lagi");
            i++;  // Menambah nilai i agar perulangan berhenti setelah 10 kali
        }
    }
}
